package pack_Maincon;

public class MainContentsBean {
	private int mNo;        // 번호
	private String mTitle;  // 전시회 제목
	private String mImg;   // 파일이름
	private String mDate1; // 전시회 시작날짜
	private String mDate2; // 전시회 종료날짜
	
	
	
	
	public int getmNo() {
		return mNo;
	}
	public void setmNo(int mNo) {
		this.mNo = mNo;
	}
	public String getmTitle() {
		return mTitle;
	}
	public void setmTitle(String mTitle) {
		this.mTitle = mTitle;
	}
	public String getmImg() {
		return mImg;
	}
	public void setmImg(String mImg) {
		this.mImg = mImg;
	}
	public String getmDate1() {
		return mDate1;
	}
	public void setmDate1(String mDate1) {
		this.mDate1 = mDate1;
	}
	public String getmDate2() {
		return mDate2;
	}
	public void setmDate2(String mDate2) {
		this.mDate2 = mDate2;
	}
	
	
}
